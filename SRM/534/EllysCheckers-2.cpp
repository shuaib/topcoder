class EllysCheckers {
public:
	string getWinner(string b) {
	
	vector<bool> dp(b.size(), false);
	
	for(int i=1;i<b.size();i++)
	{
			
				int c=0;
				for(int k=0;k<i;k++)
				{	
					if(b[k]=='o')
						c++;
				}
				if(c%2==0)
				{
					dp[i] = dp[i-1];
				}
				else
				{
					dp[i] = !dp[i-1];
				}
			
		
	}
	return dp[b.size()-1]==true?"YES" : "NO";
	}
};


