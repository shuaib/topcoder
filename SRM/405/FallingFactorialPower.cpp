class FallingFactorialPower {
public:
	
	double compute(int n, int k) {
	double result=1.0;
	if(k==0)
		return result;
	else if(k>0)
	{
		for (int i=0;i<=k-1;i++)
		{
			result=result*(n-i);
		}
		return result;
	}
	else if(k<0)
	{
		for(int i=1;i<=abs(k);i++)
		{
			result/=(n+i);
		}
		return result;
	}
		
	}
};


