class MonochromaticBoard {
public:
	int theMin(vector <string> b) {
	
	int cols = 0;
	int rows = 0;
	bool white = false;
	
	for(int i=0;i<b.size();i++)
	{
		bool flag = true;
		for(int j=0;j<b[i].size();j++)
		{
			if(b[i][j] == 'W')
			{	
				white = true;
				flag = false;
				break;
			}
		}
		if(flag)
			rows++;
	}
	
	for(int i=0;i<b[0].size();i++)
	{
		bool flag = true;
		for(int j=0;j<b.size();j++)
		{
			if(b[j][i] == 'W')
			{	
				white = true;
				flag = false;
				break;
			}
		}
		if(flag)
			cols++;
	}
		if(white)
			return cols+rows;
		else
			return min(cols, rows);
	}
};


